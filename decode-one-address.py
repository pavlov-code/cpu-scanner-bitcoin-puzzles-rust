import base58

# 1) Подставьте ваш P2PK-адрес сюда
p2pk_address = "1PWo3JeB9jrGwfHDNpdGK54CRas7fsVzXU"

# 2) Декодируем адрес из Base58Check
# Функция base58.b58decode_check автоматически декодирует строку и проверяет контрольную сумму.
decoded = base58.b58decode_check(p2pk_address)

# 3) P2PK-адрес состоит из одного элемента — самого публичного ключа.
# В некоторых реализациях публичному ключу может предшествовать один байт версии сети.
# Если это так, удаляем первый байт: public_key_bytes = decoded[1:]

# Предположим, что байт версии отсутствует, и декодированные данные — это и есть наш ключ.
public_key_bytes = decoded

# Выводим результат в шестнадцатеричном формате
print("Публичный ключ (hex):", public_key_bytes.hex())